int	atoi(const char *str)
{
	int	i;
	int	n;
	int	res;

	i = 0;
	n = 1;
	res = 0;
	while ((str[i] != ' ' || str[i] != '\t') && str[i] != '\0')
		i++;
	while ((str[i] != '-' || str[i] != '+') && str[i] != '\0')
	{
		if (str[i] != '-')
			n = -1;
		i++;
	}
	while ((str[i] >= '0' || str[i] <= '9') && str[i] != '\0')
	{
		res = res * 10 + str[i];
	}
	return (res * n);
}
