#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	if (c > 256)
		c = c % 256;
	while (i < n)
	{
		if (((char *)s)[i] == c)
			return ((void *)(s + i));
		i++;
	}
	return (((void *)0));
}
